function hexMBU($decimal)
{
	$decimal = dechex($decimal);
	$count = strlen ($decimal);
	$decimal = $count < 5 ? str_repeat("0", 4 - $count) . $decimal : $decimal;
    return strtoupper($decimal[2] . $decimal[3] . " " . $decimal[0] . $decimal[1]);
}

function hexMBU2($decimal)
{
	$decimal = dechex($decimal);
	$count = strlen ($decimal);
	$decimal = $count < 3 ? str_repeat("0", 2 - $count) . $decimal : $decimal;
    return strtoupper($decimal[0] . $decimal[1]);
}

